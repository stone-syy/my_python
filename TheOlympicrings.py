import turtle as t
t.setup(650, 500, 200, 200)
t.pensize(10)
t.pencolor('yellow')
t.circle(80)
t.penup()
t.fd(100)
t.pendown()
t.pencolor('green')
t.circle(80)
t.penup()
t.fd(40)
t.right(90)
t.fd(100)
t.left(90)
t.pendown()
t.pencolor('blue')
t.circle(80)
t.penup()
t.bk(100)
t.pendown()
t.pencolor('black')
t.circle(80)
t.penup()
t.bk(100)
t.pendown()
t.pencolor('red')
t.circle(80)
t.done()
